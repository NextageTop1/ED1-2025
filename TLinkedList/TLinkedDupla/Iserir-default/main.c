#include "TLink.h"
#include <stdio.h>
int main(){
    TLink* lista = TLink_Createlist();
    int V[] = {1, 2, 3 , 4 , 5};
    for(int i=0; i<5; i++)
        if(!TLink_insertBegin(lista, V[i]))
            printf("Erro ao inserir o elemento V[%i]=%i\n", i, V[i]);


    TLink_Print(lista);
    TLink_Print_Reverse(lista);
   

    return 0;
}